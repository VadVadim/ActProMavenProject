package com.actpro.education.cycle;

public class ForLoop {
    public void printArray() {
        int[] arr = {1, 5, 9, 6, 7, 0, 1, 2, 5, 8, 77, 333};
        for (int i = 0; i < arr.length; i++) {
            System.out.println(arr[i]);
        }
    }
}
